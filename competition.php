<p>
    The Wisconsin-Dairyland chapter of the CSTA, in conjunction with the Marquette University chapters of ACM and
    UPE, welcomes high school students with Java, Python or Scratch programming experience to participate in a morning
    of computer science problem solving and/or storytelling.

    <br /> <br />

    This Competition features three divisions:

    <br />
</p>

<h2>Java/Python Division (Blue)</h2>
<p>
    A traditional team-based programming competition, modeled on the ACM International Collegiate Programming
    Contest. Teams of three or four students will have three hours and two computers to work collaboratively to solve
    problems similar in scope to Advanced Placement Computer Science exam questions. Points will be awarded based on the
    number of problems correctly solved and the time taken to solve, with appropriate penalties for incorrect
    submissions. Development environments available will most likely include <a
        href="https://www.eclipse.org/">Eclipse</a>, <a href="https://netbeans.org/">NetBeans</a>, WordPad + JDK, <a
        href="https://www.cygwin.com/">Cygwin tools</a>, <a href="https://www.bluej.org/">BlueJ</a>, and <a
        href="https://www.jetbrains.com/idea/">IntelliJ</a>.

    <br />
    <p>
        You may find it useful to review our <a href="/assets/2019bluerules.pdf">Competition Rules</a>, and Java
        Preparation Notes <i>(Python Preparation Notes coming soon)</i>.
    </p>
</p>
<h2>Open/Scratch Division (Gold)</h2>
<p>
    Teams of two or three students will have three hours and one computer to implement a themed <a
        href='http://scratch.mit.edu/'>Scratch</a> project. Submissions will be judged on creativity, use of key Scratch
    constructs, and theme correspondence. This division will be suitable for introductory students familiar with
    Scratch.
</p>
